char* mjGladPrintName(unsigned value) {
  char* result;
  int found = 0;

  size_t n = sizeof(gl_defines) / sizeof(gl_defines[0]);
  for (size_t i = 0; i < n; i++) {
      if (gl_defines[i].value == value) {
          // printf("%s 0x%X ", gl_defines[i].name, value);
          found += 1;
          result = gl_defines[i].name;
      }
  }

  if (found == 0) {
    printf("Unknown GL define 0x%X\n", value);
    exit(1);
  } else if (found > 1) {
    printf("Multiple GL defines for 0x%X\n", value);
    exit(1);
  }

  return result;
}


